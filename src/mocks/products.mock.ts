function generateProducts(marketId: string, count: number): Product[] {
  const products: Product[] = [];
  const totalRealProducts = REAL_PRODUCTS.length;

  for (let i = 0; i < count; i++) {
    const product = REAL_PRODUCTS[i % totalRealProducts];
    const productImage = PRODUCT_IMAGES[product.name];
    
    products.push({
      id: `${marketId}-product-${i + 1}`,
      marketId,
      name: product.name,
      description: product.description,
      price: product.price,
      stock: Math.floor(Math.random() * 100) + 5,
      discount: product.discount,
      imageUrl: `https://via.placeholder.com/150?text=${encodeURIComponent(product.name)}`,
      images: productImage ? [productImage] : [PRODUCT_IMAGE_DEFAULT],
      category: product.category,
      subcategory: product.subcategory,
      createdAt: new Date('2024-01-01'),
      updatedAt: new Date('2024-01-01'),
    });
  }

  return products;
}

